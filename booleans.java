public class booleans {
    public static void main(String[] args){
        System.out.println("Boolean operations");
        //Mostly of two values either yes/no,true/false,on/off...
        int x = 10;
        int y = 9;
        System.out.println(x > y); // returns true, because 10 is higher than 9
        System.out.println(10 > 9); // returns true, because 10 is higher than 9

        int x1 = 10;
        System.out.println(x1 == 10); // returns true, because the value of x is equal to 10
        System.out.println(x1 == 15); // returns false, because 10 is not equal to 15

    }
}
